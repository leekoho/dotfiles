vim.opt.tabstop = 2       -- 一个 tab 等于 2 个空格
vim.opt.shiftwidth = 2    -- 自动缩进时的空格数量
vim.opt.expandtab = true  -- 使用空格代替 tab
vim.opt.autoindent = true -- 新行自动缩进
vim.opt.smartindent = true -- 智能缩进

vim.opt.number = true          -- 启用绝对行号
-- vim.opt.relativenumber = true  -- 启用相对行号

vim.opt.clipboard = 'unnamedplus' -- 将 Vim 的剪贴板与系统剪贴板进行关联

